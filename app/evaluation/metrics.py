import math
from rouge_score import rouge_scorer
from bert_score import score as bert_score


# Calculates the  Precision@k
# retrieved_ids: A list of document IDs retrieved by the system
# relevant_ids: A list of valid document IDs ground truth
# k: The number of first results we want to evaluate
def precision_at_k(retrieved_ids, relevant_ids, k):
    top_k = retrieved_ids[:k]  # Take only the first k results
    if not top_k:  # If no results exist, the value is zero
        return 0.0
    # Calculate how many of the first k documents are correct
    hits = sum(1 for doc_id in top_k if doc_id in relevant_ids)
    return hits / k  # Precision = Number of correct results/Number of results checked


# Calculates the Recall@k
def recall_at_k(retrieved_ids, relevant_ids, k):
    if not relevant_ids:  # If no valid documents were found, we return zero
        return 0.0
    top_k = retrieved_ids[:k]  # Take only the first k documents

    # Number of valid documents that appeared among the first results
    hits = sum(1 for doc_id in top_k if doc_id in relevant_ids)
    return hits / len(relevant_ids)  # Recall = Number of valid documents retrieved/All valid documents


# Calculates the DCG@k
def dcg_at_k(retrieved_ids, relevant_ids, k):
    dcg = 0.0
    # enumerate starts from 1 because the ranking starts from the first position
    for i, doc_id in enumerate(retrieved_ids[:k], start=1):
        rel = 1 if doc_id in relevant_ids else 0  # Determine if a document is relevant or not
        # If relevant, add its weight according to its ranking
        if rel:
            dcg += rel / math.log2(i + 1)
    return dcg


# Calculates the NDCG@k
def ndcg_at_k(retrieved_ids, relevant_ids, k):
    ideal_ids = list(relevant_ids)  # The ideal list is the same as the relevant documents
    ideal_dcg = dcg_at_k(ideal_ids, relevant_ids, min(k, len(ideal_ids)))  # Calculate the best possible DCG
    # If no relevant documents are found, return zero
    if ideal_dcg == 0:
        return 0.0
    return dcg_at_k(retrieved_ids, relevant_ids, k) / ideal_dcg  # NDCG = Actual DCG/Ideal DCG


# Calculates the ROUGE-L
# reference_answer: Reference answer
# generated_answer: Answer generated by the system
def compute_rouge_l(reference_answer, generated_answer):
    # Create a scorer for the rougeL score
    scorer = rouge_scorer.RougeScorer(["rougeL"], use_stemmer=True)
    # Calculates the score between the reference text and the generated text
    scores = scorer.score(reference_answer, generated_answer)
    # Returns F-measure
    return float(scores["rougeL"].fmeasure)

# Calculates the BERTScore for a group of answers at once
# reference_answers: List of referenced answers
# generated_answers: List of generated answers
def compute_bertscore_batch(reference_answers, generated_answers):
    # Calculates precision, recall, and F1 at the semantic representation level
    p, r, f1 = bert_score(
        generated_answers,
        reference_answers,
        lang="en",
        verbose=False
    )

    return [float(x) for x in f1]   # Converts the results from tensors to regular floats

